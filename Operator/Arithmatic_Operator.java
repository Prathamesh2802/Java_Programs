package com.java.Operator;

public class Arithmatic_Operator {
	public static void main(String args[])
	{
//		Arithmetic Operator
		int a = 12;
		int b = 24;
		System.out.println(+(a+b));
		System.out.println(+(b-a));
		System.out.println(+(a*b));
		System.out.println(+(a/b));
		System.out.println(+(a%b));
		
	}
}
