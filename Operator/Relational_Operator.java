package com.java.Operator;

public class Relational_Operator {
	public static void main(String args[])
	{
		int a=12;
		int b=12;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>=b);
		System.out.println(a<=b);
	}
}
